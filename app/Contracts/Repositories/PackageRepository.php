<?php

namespace App\Contracts\Repositories;

use App\Contracts\Interfaces\PackageInteface;
use App\Models\Package;

class PackageRepository extends BaseRepository implements PackageInteface
{
    public function __construct(Package $package)
    {
        $this->model = $package;
    }

    /**
     * Handle show method and delete data instantly from models.
     *
     * @param mixed $id
     *
     * @return mixed
     */
    public function delete(mixed $id): mixed
    {
        return $this->model->query()
            ->findOrFail($id)
            ->delete();
    }

    /**
     * Handle get the specified data by id from models.
     *
     * @param mixed $id
     *
     * @return mixed
     */
    public function show(mixed $id): mixed
    {
        //
    }

    /**
     * Handle the Get all data event from models.
     *
     * @return mixed
     */
    public function get(): mixed
    {
        return $this->model->query()
            ->get();
    }

    /**
     * Handle store data event to models.
     *
     * @param array $data
     *
     * @return mixed
     */
    public function store(array $data): mixed
    {
        $feature = $this->model->query()
            ->create($data);

        foreach ($data['name_feature'] as $item) {
            $data['name_feature'] = $item;

            $feature->packageFeatures()->create($data);
        }

        return $feature;
    }

    /**
     * Handle show method and update data instantly from models.
     *
     * @param mixed $id
     * @param array $data
     *
     * @return mixed
     */
    public function update(mixed $id, array $data): mixed
    {
        return $this->model->query()
            ->findOrFail($id)
            ->update($data);
    }
}
